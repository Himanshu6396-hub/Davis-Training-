# Function to calculate addition 
def addition(a,b):
    return (a+b)
# Function to calculate subtraction 
def subtraction(a,b):
    return (a-b)
#Function to calculate product 
def product (a,b):
    return (a*b)
# Function to calculate division 
def division(a,b):
    return (a/b)
# Function to calculate modules 
def modules(a,b):
    return (a%b)
